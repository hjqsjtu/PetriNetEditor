package client;

/**
 * Vyctovy typ pro mod prace se siti. viz. {@link MainGui}
 * @author Kvita Jakub
 */
public enum Buttons {
	/** Mod vytvareni hran. */
	ARC,
	/** Mod vytvareni prechodu nebo hybani s objekty. */
	TRANS,
	/** Mod vytvareni mist nebo hybani s objekty. */
	PLACE,
	/** Mod mazani objektu. */
	DELETE,
	/** Mod vyberu objektu. */
	SELECT,
	/** Vlozeni textu do objektu */
	INSERT,
	/** Pocatecni mod. Hybani s objekty. */
	FALSE
}

